package com.joaoandrade.pastelaria.api.model;

public class EstadoModel {
	private Long id;
	private String nome;

	public EstadoModel() {
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
